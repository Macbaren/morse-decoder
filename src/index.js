const MORSE_TABLE = {
  '.-': 'a',
  '-...': 'b',
  '-.-.': 'c',
  '-..': 'd',
  '.': 'e',
  '..-.': 'f',
  '--.': 'g',
  '....': 'h',
  '..': 'i',
  '.---': 'j',
  '-.-': 'k',
  '.-..': 'l',
  '--': 'm',
  '-.': 'n',
  '---': 'o',
  '.--.': 'p',
  '--.-': 'q',
  '.-.': 'r',
  '...': 's',
  '-': 't',
  '..-': 'u',
  '...-': 'v',
  '.--': 'w',
  '-..-': 'x',
  '-.--': 'y',
  '--..': 'z',
  '.----': '1',
  '..---': '2',
  '...--': '3',
  '....-': '4',
  '.....': '5',
  '-....': '6',
  '--...': '7',
  '---..': '8',
  '----.': '9',
  '-----': '0',
};

function decode(expr) {
  const toWord = (str) => {
    return str
      .match(/.{10}/g)
      .map((it) =>
        it
          .match(/.{2}/g)
          .filter((ch) => ch !== '00')
          .map((el) => (el === '10' ? '.' : '-'))
          .join('')
      )
      .map((code) => MORSE_TABLE[code])
      .join('');
  };

  return expr
    .split('**********')
    .map((it) => toWord(it))
    .join(' ');
}

